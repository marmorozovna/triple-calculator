package ru.morozevich.triplecalculator.util;

public class Calculator {
    public static int sum(int a, int b) {
        return a + b;
    }

    ;

    public static int deduct(int a, int b) {
        return a - b;
    }

    ;

    public static int multiply(int a, int b) {
        return a * b;
    }

    ;
}
